	constexpr math::Vec4 convert(const std::uint32_t color)
	{
		return {
			((color & 0xFF000000u) >> 24) / 255.0f,
			((color & 0x00FF0000u) >> 16) / 255.0f,
			((color & 0x0000FF00u) >> 8) / 255.0f,
			(color & 0x000000FFu) / 255.0f
		};
	}

